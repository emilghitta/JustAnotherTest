package javaTraining.Operators;

public class RelationalOperators {
    public static void main(String[] args){
        System.out.println(10 > 4);
        System.out.println(5 < 4);
        System.out.println(10 >= 10);
        System.out.println(10 <= 10);
        System.out.println(4==4);
        System.out.println(5!=4);
    }
}
